class Solution:
    def reverseList(self, head: ListNode) -> ListNode:
        prev = None       # Изначально предыдущего узла нет (это будет конец списка)
        curr = head       # Начинаем с головы списка

        while curr is not None:
            # 1. Запоминаем следующий узел, чтобы не потерять его
            next_node = curr.next 
            
            # 2. Разворачиваем стрелочку: текущий узел теперь указывает на предыдущий
            curr.next = prev 
            
            # 3. Сдвигаем наши "указатели" на один шаг вперед
            prev = curr       # Теперь текущий узел становится "предыдущим"
            curr = next_node  # А сохраненный следующий узел становится "текущим"

        # Когда цикл закончится, curr будет None, а prev будет указывать на новую голову списка
        return prev